package jdev.modulos.modulo8;

public class DivisoesRestoModulo {

	public static void main(String[] args) {

		/*Módulo: Resto da divisão %*/
		
		double carro = 9;
		double pessoa = 2;
		
		double resto = carro % pessoa; /*MOD*/
		
		System.out.println("Sobraram exatemente : "+resto+" carro(s).");
		
	}

}
